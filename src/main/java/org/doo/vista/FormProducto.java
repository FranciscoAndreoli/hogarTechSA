    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {                                           
        // TODO add your handling code here:
        String nombre = this.getTxtNombre().getText();
        String precio = this.getTxtPrecio().getText();
        String color = this.getTxtColor().getText();
        String marca = this.getTxtMarca().getText();
        String modelo = this.getTxtModelo().getText();
        String stock = this.getTxtStock().getText();
                
        if (!nombre.equals("") && !precio.equals("") && !color.equals("") && !marca.equals("") && !modelo.equals("") && !stock.equals("")) {
            boolean resultado = controlador.guardar(nombre.toUpperCase(), Double.valueOf(precio), color, marca, modelo, Integer.valueOf(stock));
            if (resultado) {
                actualizaTabla();
            }
            limpiaVista();
        } else {
            imprimeMensaje(new Exception("Todos los campos son obligatorios"));
        }
    }

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
         if (this.getTblProductos().getSelectedRow() == -1) {
            imprimeMensaje(new Exception("Debe seleccionar una fila"));
            return;
        }
        int respuesta = JOptionPane.showConfirmDialog(this, "¿Seguro desea eliminar este producto?", "Información", JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (respuesta == JOptionPane.OK_OPTION) {
            int legajo = Integer.parseInt(modeloTbl.getValueAt(this.getTblProductos().getSelectedRow(), 0).toString());
            boolean resultado = controlador.borrar(legajo);
            if (resultado) {
                actualizaTabla();
            } else {
                imprimeMensaje(new Exception("Ocurrio un error al eliminar el alumno, es probable que tenga inscripciones asociadas"));
            }
            limpiaVista();
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

