    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        if (this.getTblProductos().getSelectedRow() == -1) {
            imprimeMensaje(new Exception("Debe seleccionar una fila"));
            return;
        }
        int respuesta = JOptionPane.showConfirmDialog(this, "¿Seguro desea modificar este producto?", "Información", JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (respuesta == JOptionPane.OK_OPTION) {
            int id = Integer.parseInt(modeloTbl.getValueAt(this.getTblProductos().getSelectedRow(), 0).toString());
            String nombre = this.getTxtNombre().getText();
            String precio = this.getTxtPrecio().getText();
            String color = this.getTxtColor().getText();
            String marca = this.getTxtMarca().getText();
            String modelo = this.getTxtModelo().getText();
            String stock = this.getTxtStock().getText();
        boolean resultado = controlador.modificar(id, nombre.toUpperCase(), Double.valueOf(precio), color, marca, modelo, Integer.valueOf(stock));
            if (resultado) {
                actualizaTabla();
            }
            limpiaVista();
        }
    }//GEN-LAST:event_btnModificarActionPerformed

